package com.katafoni.filemanager.common.security.auth;

import com.google.common.collect.Sets;
import org.springframework.security.core.GrantedAuthority;
import org.springframework.security.core.authority.SimpleGrantedAuthority;

import java.util.Set;
import java.util.stream.Collectors;

public enum ApplicationUserRole {


    STANDARD_USER(Sets.newHashSet(ApplicationUserPermission.FILE_READ,
            ApplicationUserPermission.FILE_WRITE,
            ApplicationUserPermission.FILE_DELETE,
            ApplicationUserPermission.FILEINFO_READ)),

    ADMINISTRATOR(Sets.newHashSet(ApplicationUserPermission.EXTENSION_ADD,
                  ApplicationUserPermission.EXTENSION_DELETE,
                  ApplicationUserPermission.EXTENSION_READ));

    private final Set<ApplicationUserPermission> permissions;

    ApplicationUserRole(Set<ApplicationUserPermission> permissions) {
        this.permissions = permissions;
    }

    public Set<ApplicationUserPermission> getPermissions() {
        return permissions;
    }

    public Set<GrantedAuthority> getGrantedAuthorities() {
        Set<GrantedAuthority> permissions = getPermissions().stream()
                .map(permission -> new SimpleGrantedAuthority(permission.getPermission()))
                .collect(Collectors.toSet());
        permissions.add(new SimpleGrantedAuthority("ROLE_" + this.name()));
        return permissions;
    }
}
